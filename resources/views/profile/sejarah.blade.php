    <p style="text-indent: 45px;">

                    Wayang Riders merupakan sebuah komunitas yang dibentuk pada 12 Februari tahun 2000,
                komunitas ini didirikan oleh Ricky Darmawan Noya berlokasi di Kota Cimahi,komunitas ini
                berasal dari salah satu tongkrongan yang ingin memamerkan motor motor mereka dengan cara
                membuat komunitas.Sekitar Tahun 2010-an,Komunitas Wayang Riders mulai terkenal,karena salah satu anggota
                komunitas wayang riders yaitu Cecep Hendrawan mengikuti balapan motor di sentul dengan membawa nama komunitas
                Wayang Riders,Cecep Hendrawan berhasil juara 1,dan membawa harum nama Komunitas ini</p><br>

    <p style="text-indent: 45px;">
                    Salah satu kegiatan yang sering mereka lakukan adalah nongKrong sana sini, dan mengotak-atik motor,
                dari hobi sebagian anggotanya yang suka mengotak-ngatik motor mulai lah mereka suka mengikuti balapan liar maupun balapan resmi.
                Dengan kegiatan itu lah nama Wayang Riders mulai di kenal di Kota Cimahi, dan mulai mempunyai regenerasi dari tahun ke tahun.
                Kalau bisa di ibaratkan ternyata Wayang Riders itu ibarat “VIRUS” yang menyebar dengan cepat, hampir dibeberapa wilayah ada anak Wayang Riders.</p>
            <br>

            <p style="text-indent: 45px;">
                Wayang Riders terkenal dengan anak motor nya, yang hampir setiap minggu melakukan kumpul-kumpul bersama,
             baik di wilayah masing-masih ataupun melakukan kumpul bersama yang biasa di lakukan di tempat pertama kali nama Wayang Riders muncul.</p>
            <br>
            <p style="text-indent: 45px;">
                Wayang Riders mulai bergeliat dan mereka menyepakati untuk memakai bendera berwarna “Abu – Kuning” yang memiliki filosofi tersendiri.
             Warna Abu mencerminkan tali persaudaraan yang tidak terukur dengan apapun sedangkan Warna Kuning sendiri bermakna mewarnai dengan kata lain
             tali persaudaraan itu terbentuk semakin erat dikarenakan oleh karakteristik dari pada anggotanya yang berbeda-beda tetapi mempunyai tujuan
             yang sama yaitu menjadi Keluarga Besar Wayang Riders.</p>
            <br>
            <p style="text-indent: 45px;">
                Dikarenakan semakin lama Wayang Riders berdiri, ternyata anggotanya semakin banyak dan mulai merambah keluar Kota Cimahi malahan sampai keluar Propinsi.
                 Oleh karena itu sekitar Awal Tahun 2015 an diadakan lah suatu Jambore Nasional yang dihadiri oleh para anggota dari segala penjuru. Acara Jambore
                 Nasiona yang di adakan di Pangandaran itu mempunyai Visi Misi yang sangat bagus sekali, selain sebagai ajang temu kangen para anggota, merayakan hari
                 jadi Wayang Riders yang terbentuk tanggal 12 Februari, acara Jambore ini juga menghasilkan beberapa keputusan yang sangat penting sekali, salah satunya
                 adalah menjadikan Wayang Riders sebagai Organisasi.

            </p>

